    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        if (tfNombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe llenar el nombre.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String nombre = tfNombre.getText().trim();
        String tipo = (String) cbTipo.getSelectedItem();

        Arma armaBuscada = consultarArma(nombre, tipo);
        if (armaBuscada == null) {
            JOptionPane.showMessageDialog(this, "El arma no existe.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        tfResultado.setText(armaBuscada.toString());
    }//GEN-LAST:event_btnConsultarActionPerformed

    public Arma consultarArma(String nombre, String tipo) {
        List<Arma> lista = servicioArma.getArmas();

        for (Arma arma : lista) {
            if (arma.getNombre().equals(nombre) && arma.getClass().getSimpleName().equals(tipo)) {
                return arma;
            }
        }

        return null;
    }


